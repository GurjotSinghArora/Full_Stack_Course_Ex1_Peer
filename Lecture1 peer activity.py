#implementing addition

add= int(first_number)+int(second_number)

#subtraction

sub=int(first_number)-int(second_number)

# multiplication

multi=int(first_number)*int(second_number)

# power

power=int(first_number)**int(second_number)

#division

div=int(first_number)/int(second_number)


print("The addition of two numbers is: " + str(add))
print("The subtraction of two numbers is: " + str(sub))
print("The multiplication of two numbers is: " + str(multi))
print("The power of two numbers is: " + str(power))
print("The division of two numbers is: " + str(div))

